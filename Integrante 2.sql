CREATE VIEW vw_Directorio_General
AS
SELECT PERSONA.CEDULA AS Cedula_Cliente,
PERSONA.NOMBRE AS Nombre_Cliente,
PERSONA.CORREO AS Correo_Cliente,
CONCAT(PERSONA.CALLE, ' ', PERSONA.NOMBRE, ', ' , PERSONA.SECTOR) AS Direccion,
TELEFONO_PERSONA.NUM_PERSONA AS Telefono,
CLIENTE.FECHA_REGISTRO AS Fecha_Registro_Cliente,
MASCOTA.ID_MASCOTA AS ID_Mascota,
MASCOTA.NOMBRE_MASCOTA AS Nombre_Mascota,
MASCOTA.ESPECIE AS Especie_Mascota,
MASCOTA.RAZA AS Raza_Mascota,
MASCOTA.FECHA_NACIMIENTO AS Fecha_Nacimiento_Mascota,
MASCOTA.PESO_ACTUAL AS Peso_Mascota

FROM CLIENTE
JOIN PERSONA ON CLIENTE.CEDULA_CLIENTE = PERSONA.CEDULA
LEFT JOIN TELEFONO_PERSONA ON PERSONA.CEDULA = TELEFONO_PERSONA.CEDULA_PERSONA
LEFT JOIN MASCOTA ON CLIENTE.CEDULA_CLIENTE = MASCOTA.CEDULA_CLIENTE;

Select * from vw_Directorio_General





