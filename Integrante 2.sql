CREATE FUNCTION fn_Calcular_Edad (@FechaNacimiento DATE)
RETURNS INT 
AS
BEGIN
DECLARE @Edad INT
IF @FechaNacimiento IS NULL
RETURN NULL;

SET @Edad=(
CAST(CONVERT(VARCHAR(10), GETDATE(), 112) AS INT)
- CAST(CONVERT(VARCHAR(10), @FechaNacimiento, 112) AS INT))/10000;
RETURN @Edad;
END

SELECT MASCOTA.NOMBRE_MASCOTA, MASCOTA.FECHA_NACIMIENTO, dbo.fn_Calcular_Edad(FECHA_NACIMIENTO) AS Edad
FROM MASCOTA


