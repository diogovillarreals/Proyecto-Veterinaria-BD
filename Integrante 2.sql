CREATE FUNCTION fn_Calcular_Total_Insumos(@IDCita INT)
RETURNS DECIMAL(10, 2)
AS
BEGIN
DECLARE @TotalGastado DECIMAL(10, 2);

SELECT @TotalGastado = SUM(INSUMO_CONSUMIDO.Cantidad_Utilizada * INSUMO.PRECIO_UNITARIO)
FROM INSUMO_CONSUMIDO
JOIN INSUMO ON INSUMO_CONSUMIDO.ID_Insumo = INSUMO.ID_INSUMO
WHERE INSUMO_CONSUMIDO.ID_Cita = @IDCita;

RETURN ISNULL(@TotalGastado, 0.00);
END;

